         <h2>Tribute</h2>
         <p><strong>500 days of summer</strong> is movie ahead of its time. It does not exclude itself from the annoying cliches of 
            romantic films but rather it challenges them.  It challenges the love at first sight BS. The film lowers your expectations up front that it is 
            not a love story but still makes you wishing it was through out the movie. It plays around the role fate plays
            in two individuals falling in love together. There are two warring themes in the movie. On one corner you have the "soulmate" ideology that there is someone out
            who was destined to be with you,who likes the same things you like and is just the perfect match for you. On the other corner you have the ideology that there is no such
            as love all together,its just fantasy and there is no destined perfect person for everyone. 
        </p>
        <p>At the end it never really decides a clear winner. Rather it gives the feeling that there is something valuable to learn
         from both these two ideologies. That love exists but there isn't a perfect person who would agree with everything with you and like 
         exactly the same things you like. Its a movie that would end up as a classic and it has had a great cultural impact. People connect to how realistic the
         movie is. Its referenced several time across pop culture usually as that one realistic romantic film.            
        </p> 
        <h2>Music</h2>
        <p>500 days of summer has the best collection of music and soundtracks i have ever seen in a film. The music was just well chosen.
            Each track fit every scene it covered and perfectly conveyed the emotions of every scene. My favorite of the tracks were from the very talented Russian born American singer 
            Regina Spektor. The first was her song "Us" played at the beginning of the film and perfectly conveyed the nostaligic feeling the scene was going for.
            The second was "hero" which just perfect as it was played at a major turning point in the movie. 
        </p>
